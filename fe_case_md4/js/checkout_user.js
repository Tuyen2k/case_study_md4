let acc = JSON.parse(localStorage.getItem("account"))

function getAccount() {
    let id_account = acc.id;
    $.ajax({
        type: "GET",
        url: `http://localhost:8080/api/auth/${id_account}`,
        success: function (data) {
            console.log(data)
            displayBillAddress(data);
        }
    })
}
